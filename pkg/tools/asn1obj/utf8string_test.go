package asn1obj

import (
	"bytes"
	"testing"
)

var utf8StringTests = []asn1StringTest{
	// empty string
	{
		content:         "",
		expectedEncoded: []byte{0x0C, 0x00},
	},

	// small
	{
		content:         "help",
		expectedEncoded: []byte{0x0C, 0x04, 0x68, 0x65, 0x6C, 0x70},
	},
	{
		content:         "some th!n ~*",
		expectedEncoded: []byte{0x0C, 0x0C, 0x73, 0x6F, 0x6D, 0x65, 0x20, 0x74, 0x68, 0x21, 0x6E, 0x20, 0x7E, 0x2A},
	},

	// larger
	{
		content: "Some sentence! With DiFFerent st@ff in it &^ _",
		expectedEncoded: []byte{0x0C, 0x2E, 0x53, 0x6F, 0x6D, 0x65, 0x20, 0x73, 0x65, 0x6E, 0x74, 0x65, 0x6E, 0x63, 0x65, 0x21, 0x20, 0x57, 0x69, 0x74,
			0x68, 0x20, 0x44, 0x69, 0x46, 0x46, 0x65, 0x72, 0x65, 0x6E, 0x74, 0x20, 0x73, 0x74, 0x40, 0x66, 0x66, 0x20, 0x69, 0x6E, 0x20, 0x69, 0x74, 0x20,
			0x26, 0x5E, 0x20, 0x5F},
	},

	// uncommon
	{
		content:         "Â£",
		expectedEncoded: []byte{0x0C, 0x04, 0xC3, 0x82, 0xC2, 0xA3},
	},
	{
		content:         "日本語",
		expectedEncoded: []byte{0x0C, 0x09, 0xE6, 0x97, 0xA5, 0xE6, 0x9C, 0xAC, 0xE8, 0xAA, 0x9E},
	},
	{
		content:         "😆",
		expectedEncoded: []byte{0x0C, 0x04, 0xF0, 0x9F, 0x98, 0x86},
	},

	// mixed uncommon
	{
		content: "Some ৳ d@fferent 👩‍❤️‍👩 text 丅 with ¤ mixed 丸 stuff 丷 to 🧞‍♂️ try ₳ !",
		expectedEncoded: []byte{0x0C, 0x67, 0x53, 0x6F, 0x6D, 0x65, 0x20, 0xE0, 0xA7, 0xB3, 0x20, 0x64, 0x40, 0x66, 0x66, 0x65, 0x72,
			0x65, 0x6E, 0x74, 0x20, 0xF0, 0x9F, 0x91, 0xA9, 0xE2, 0x80, 0x8D, 0xE2, 0x9D, 0xA4, 0xEF, 0xB8, 0x8F, 0xE2, 0x80, 0x8D, 0xF0,
			0x9F, 0x91, 0xA9, 0x20, 0x74, 0x65, 0x78, 0x74, 0x20, 0xE4, 0xB8, 0x85, 0x20, 0x77, 0x69, 0x74, 0x68, 0x20, 0xC2, 0xA4, 0x20,
			0x6D, 0x69, 0x78, 0x65, 0x64, 0x20, 0xE4, 0xB8, 0xB8, 0x20, 0x73, 0x74, 0x75, 0x66, 0x66, 0x20, 0xE4, 0xB8, 0xB7, 0x20, 0x74,
			0x6F, 0x20, 0xF0, 0x9F, 0xA7, 0x9E, 0xE2, 0x80, 0x8D, 0xE2, 0x99, 0x82, 0xEF, 0xB8, 0x8F, 0x20, 0x74, 0x72, 0x79, 0x20, 0xE2,
			0x82, 0xB3, 0x20, 0x21},
	},
}

// run all the tests
func TestUtf8String(t *testing.T) {
	for _, utf8Test := range utf8StringTests {
		// run test
		out := UTF8String(utf8Test.content)
		if !bytes.Equal(out, utf8Test.expectedEncoded) {
			t.Errorf("UTF8String '%s' expected '%X' but got '%X'", utf8Test.content, utf8Test.expectedEncoded, out)
		}
	}
}
