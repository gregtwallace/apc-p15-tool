// run one BitString test (needed because panic crashes the entire function which
// breaks the test loop)
func oneBitStringTest(t *testing.T, strTest asn1StringTest) {
	// panic handling
	defer func() {
		// did it panic but shouldn't have?
		r := recover()
		if r != nil && !strTest.shouldPanic {
			t.Errorf("Bitstring of '%s' panicked but should have produced '%X' (%s)", strTest.content, strTest.expectedEncoded, r)
		}
	}()

	// run test
	out := BitString(strTest.content)
	if !bytes.Equal(out, strTest.expectedEncoded) {
		t.Errorf("Bitstring of '%s' expected '%X' but got '%X'", strTest.content, strTest.expectedEncoded, out)
	}

	// should not get here if panic was expected
	if strTest.shouldPanic {
		t.Errorf("Bitstring of '%s' expected panic, but got '%X'", strTest.content, out)
	}
}

// BitString test func
func TestBitString(t *testing.T) {
	for _, bsTest := range bitStringTests {
		oneBitStringTest(t, bsTest)
	}
}

